Write-Host "3. Purchase a hosting plan"
Write-Host "4. Access your hosting control panel (usually cPanel)"
Write-Host "5. Use the File Manager or FTP to upload all website files to the public_html directory"
Write-Host "6. Your website will be available at your domain name"
