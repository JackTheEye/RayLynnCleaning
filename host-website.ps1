# Host-Website.ps1
# This script provides instructions for hosting the RAYLYNN CLEANING SERVICES website

Write-Host "RAYLYNN CLEANING SERVICES - Website Hosting Guide" -ForegroundColor Cyan
Write-Host "-------------------------------------------------------------" -ForegroundColor Cyan
Write-Host ""

Write-Host "Option 1: Hosting with GitHub Pages" -ForegroundColor Green
Write-Host "-------------------------------------------------------------" -ForegroundColor Yellow
Write-Host "1. Create a GitHub account if you don't have one: https://github.com/signup"
Write-Host "2. Create a new repository named 'raylynncleaningservices.github.io'"
Write-Host "3. Upload all website files to this repository"
Write-Host "4. Go to repository Settings > Pages"
Write-Host "5. Set Source to 'main' branch and folder to '/ (root)'"
Write-Host "6. Click Save"
Write-Host "7. Your website will be available at: https://raylynncleaningservices.github.io"
